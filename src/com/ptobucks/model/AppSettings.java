package com.ptobucks.model;

public class AppSettings {
	
	private boolean receiptNo;
	private boolean receiptImage;
	
	
	public boolean isReceiptNo() {
		return receiptNo;
	}
	public void setReceiptNo(boolean receiptNo) {
		this.receiptNo = receiptNo;
	}
	public boolean isReceiptImage() {
		return receiptImage;
	}
	public void setReceiptImage(boolean receiptImage) {
		this.receiptImage = receiptImage;
	}
	
	

}
